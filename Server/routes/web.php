Route::group(['middleware' => 'auth'], function () {
	Route::resource('user', 'App\Http\Controllers\UserController', ['except' => ['show']]);
    Route::resource('contest', 'App\Http\Controllers\ContestController', ['except' => ['show']]);
    Route::resource('match', 'App\Http\Controllers\MatchController', ['except' => ['show']]);
    Route::resource('team', 'App\Http\Controllers\TeamController', ['except' => ['show']]);
    Route::resource('player', 'App\Http\Controllers\PlayerController', ['except' => ['show']]);
    Route::resource('payment', 'App\Http\Controllers\PaymentController', ['except' => ['show']]);
	Route::get('profile', ['as' => 'profile.edit', 'uses' => 'App\Http\Controllers\ProfileController@edit']);
	Route::put('profile', ['as' => 'profile.update', 'uses' => 'App\Http\Controllers\ProfileController@update']);
	Route::put('profile/password', ['as' => 'profile.password', 'uses' => 'App\Http\Controllers\ProfileController@password']);
});

